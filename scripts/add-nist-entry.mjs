#!/usr/bin/env node
import { createClient } from '@supabase/supabase-js'
import { readFileSync } from 'fs'
import { resolve } from 'path'

const envFile = readFileSync(resolve(import.meta.dirname, '..', '.env.local'), 'utf-8')
for (const line of envFile.split('\n')) {
  const t = line.trim()
  if (!t || t.startsWith('#')) continue
  const i = t.indexOf('=')
  if (i === -1) continue
  process.env[t.slice(0, i)] = t.slice(i + 1)
}

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY
)

const entries = [
  {
    category: 'appearance',
    title: 'NIST NCCoE Response: Software and AI Agent Identity and Authorization',
    content: `Submitted March 30, 2026 to NIST NCCoE (AI-Identity@nist.gov) in response to the concept paper "Accelerating the Adoption of Software and AI Agent Identity and Authorization." Comment period: February 5 - April 2, 2026. Core argument: authorization with sufficient context is governance. "Is this permitted" and "should this happen" collapse into the same question when the authorization framework carries enough context to evaluate appropriateness at the point of tool execution. Brian advocated for composing existing standards (MCP, OAuth 2.1, SPIFFE/SPIRE, NGAC) at the MCP server enforcement boundary rather than building new protocols. Key positions: Identification -- persistent agent identity with ephemeral authorization context; the agent is a stable registered entity, what changes per-session is scopes, accumulated sensitivity, and delegation chain. Authentication -- mutual authentication between agents and MCP servers using SPIFFE SVIDs, flagging that a compromised MCP server an agent trusts implicitly is a data exfiltration vector. Authorization -- progressive scope management with just-in-time grants modeled after step-up authentication for human users. Auditing -- tamper-evident hash-chained audit records generated by the MCP server (not the agent) with OpenTelemetry trace context for cross-system correlation, explicitly distinguished from blockchain approaches. Prompt injection -- separation of duties between reasoning and enforcement layers as primary defense; if the enforcement boundary is architecturally independent of the reasoning layer, compromising reasoning doesn't compromise authorization. Distinguishing contributions: provenance chains linking invocations cryptographically to predecessors, governance enforcement at Layer 7 where policy changes take effect on next invocation, revocation propagation models referencing OpenID Shared Signals and CAEP, and cross-session drift detection via periodic re-certification through SPIFFE attestation.`,
    tags: ['nist', 'nccoe', 'ai-governance', 'identity', 'authorization', 'mcp', 'spiffe', 'oauth', 'ngac', 'agent-security', 'policy-response', 'standards'],
    priority: 5,
    source: 'manual',
    is_active: true,
    confidence: 1.0,
  },
]

for (const entry of entries) {
  const { data, error } = await supabase
    .from('knowledge_base')
    .insert(entry)
    .select('id, title, category')

  if (error) {
    console.error(`FAILED: "${entry.title}" --`, error.message)
  } else {
    console.log(`INSERTED [${data[0].id.slice(0, 8)}] ${data[0].category}: "${data[0].title}"`)
  }
}

console.log('Done.')
